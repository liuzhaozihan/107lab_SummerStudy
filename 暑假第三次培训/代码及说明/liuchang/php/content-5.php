《青藤山人徐渭》被中国美术馆收藏。《石涛》入选“97全国中国画人物画展”，《冬心先生》入选“纪念毛主席延安文艺座谈会讲话发表60周年全国
美展”。出版有《素描技法论要》和《丁中一西部写生画集》等。1993年以来，先后5次在台湾和德国举办个人画展，并得到高度评价。画作与论文曾
在《美术》《国画家》《书与画》《美术界》《中国画》等刊物上发表和作专题介绍。
