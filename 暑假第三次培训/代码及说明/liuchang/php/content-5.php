丁中一，男，1937年3月生于上海，1960年毕业于浙江美术学院（现中国美院）中国画系，同年赴河南工作至今。现为河南大学美术学院教授
、硕士生导师，郑州大学特聘教授，中国美协会员，河南美协顾问、省文联委员、省文史馆馆员，河南省优秀专家。出席全国第七、八次文代会
