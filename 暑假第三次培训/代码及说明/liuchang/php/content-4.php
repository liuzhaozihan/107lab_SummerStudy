发布时间：2020-03-25 09:57:42   作者：统战部
侯镜如(1902-1994)，字心朗，后以字行，河南永城人，中共党员，民革成员，中国著名社会活动家、革命家，生前担任中国人民政治协商会议
第八届全国委员会副主席、中国国民党革命委员会名誉主席、黄埔同学会会长、中国和平统一促进会会长、河南大学校友会顾问。

侯镜如出生在一个书香门第，在长辈的教诲下，他自幼饱览诸子百家著作。清末民初大变革时代受进步思想的启蒙，少年时代就追求真理，渴望民
主。但是，真正使他认识社会、认识人生的，还是他在河南大学6年的学习期间所接触到的马克思列宁主义思想。1918年，他考入河南留学欧美预备
学校英文专业。时值祖国内忧外患多事之秋，刚刚步入青年的侯镜如，积极参加了开封的新文化运动、声援五四运动等。国文教员江梦霞(中共开封
市委书记)等，公开在讲坛上宣传共产主义思想，讲解辩证唯物主义和历史唯物主义观点。侯镜如就是在这时候接受马克思主义理论的。于是，他毫
不犹豫地选择了革命道路，经江梦霞介绍，加入社会主义青年团。

在河南留学欧美预备学校，侯镜如就读的英文科专业开设26门课程总计6232个学时，分量重，内容深，加上大部分课程使用英文原版教材，更使学
生应接不暇。在这种情况下，侯镜如学习十分刻苦，各门功课成绩良好。他有广泛的业余爱好，是全校公认的才子、品学兼优的高材生。
